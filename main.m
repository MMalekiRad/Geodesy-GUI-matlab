% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

ph=str2num(get(handles.ph,'string'))
La=str2num(get(handles.La,'string'))
h=str2num(get(handles.h,'string'))
xo=str2num(get(handles.x,'string'))
yo=str2num(get(handles.y,'string'))
zo=str2num(get(handles.z,'string'))
r1=get(handles.radiobutton1,'value')
r2=get(handles.radiobutton2,'value')
r3=get(handles.radiobutton3,'value')
r4=get(handles.radiobutton4,'value')
distance = get(handles.popupmenu2, 'value');
angle = get(handles.popupmenu1, 'value');

if angle==1
   ph=(ph*180)/pi;
   La=(La*180)/pi;
elseif angle==2
    ph=(ph*200)/pi;
    La=(La*200)/pi;
elseif angle==3
    ph=ph;
    La=La;
end

if r1==1
a=6400000;
b=6378590;
elseif r2==1
 a=6378388;
 b=6356911.946;
elseif r3==1
 a=6378200;
 b=6356818.17;
elseif r4==1
 a=6378249.145;
 b=6356514.870;
end


N=a^2/(b^2*(sin(ph))^2+a^2*(cos(ph))^2)^.5
x=(N+h)*cos(ph)*cos(La)+xo
y=(N+h)*cos(ph)*sin(La)+yo
z=(N*(b^2/a^2)+h)*sin(ph)+zo
if distance==1
N=a^2/(b^2*(sin(ph))^2+a^2*(cos(ph))^2)^.5
x=(N+h)*cos(ph)*cos(La)+xo
y=(N+h)*cos(ph)*sin(La)+yo
z=(N*(b^2/a^2)+h)*sin(ph)+zo
elseif distance==2    
N1=N*100;
h1=h*100;
a1=a*100;
b1=b*100;
N2=a^2/(b1^2*(sin(ph))^2+a1^2*(cos(ph))^2)^.5
x2=(N1+h)*cos(ph)*cos(La)+xo
y2=(N1+h1)*cos(ph)*sin(La)+yo
z2=(N1*(b1^2/a1^2)+h)*sin(ph)+zo
elseif distance==3
N3=N*1000;
h3=h*1000;
a3=a*1000;
b3=b*1000;
N=a3^2/(b3^2*(sin(ph))^2+a3^2*(cos(ph))^2)^.5
x=(N3+h3)*cos(ph)*cos(La)+xo
y=(N3+h3)*cos(ph)*sin(La)+yo
z=(N3*(b3^2/a3^2)+h3)*sin(ph)+zo
set(handles.edit1,'string',x)
set(handles.edit2,'string',y)
set(handles.edit3,'string',z)
end
